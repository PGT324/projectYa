package vo;

public class HPointVO {
	
	private int hpoint_idx;
	private int users_idx;
	private int value;
	private int h_res_idx;
	
	public HPointVO() {;}

	public int getHpoint_idx() {
		return hpoint_idx;
	}

	public void setHpoint_idx(int hpoint_idx) {
		this.hpoint_idx = hpoint_idx;
	}

	public int getUsers_idx() {
		return users_idx;
	}

	public void setUsers_idx(int users_idx) {
		this.users_idx = users_idx;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getH_res_idx() {
		return h_res_idx;
	}

	public void setH_res_idx(int h_res_idx) {
		this.h_res_idx = h_res_idx;
	}
	
	
	

}

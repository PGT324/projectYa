package vo;

public class MPointVO {
	
	private int mpoint_idx;
	private int users_idx;
	private int value;
	private int m_res_idx;
	
	public MPointVO() {;}

	public int getMpoint_idx() {
		return mpoint_idx;
	}

	public void setMpoint_idx(int mpoint_idx) {
		this.mpoint_idx = mpoint_idx;
	}

	public int getUsers_idx() {
		return users_idx;
	}

	public void setUsers_idx(int users_idx) {
		this.users_idx = users_idx;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getM_res_idx() {
		return m_res_idx;
	}

	public void setM_res_idx(int m_res_idx) {
		this.m_res_idx = m_res_idx;
	}
	
	
	
	

}

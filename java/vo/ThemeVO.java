package vo;

public class ThemeVO {
	
	private int theme_idx;
	private String name;
	private String image;
	
	public ThemeVO() {;}

	public int getTheme_idx() {
		return theme_idx;
	}

	public void setTheme_idx(int theme_idx) {
		this.theme_idx = theme_idx;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
	

}
